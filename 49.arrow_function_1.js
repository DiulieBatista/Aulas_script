//Arrow Functions Simples -Representando funções com setas =>
function calc1(x,y) {
    
    return x*y;
}
console.log(calc1(3,7));
console.log("----------------------------------------------");
let calc2=function (x,y) {
return x*y
}
console.log(calc2(5,2));
console.log("----------------------------------------------");

let calc3=(x,y)=> x*y;
console.log(calc3(3,6));
console.log("----------------------------------------------");